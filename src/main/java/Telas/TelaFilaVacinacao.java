    private void registraVacinacaoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registraVacinacaoButtonActionPerformed
        // TODO add your handling code here:
        try{
        int escolha = JOptionPane.showConfirmDialog(this, "Confirma a aplicação desta vacinação? ");     
        if (escolha == JOptionPane.YES_OPTION){
            
        Date data = new Date();
        VacinacaoDAO vacinacaoDao = new VacinacaoDAO();
        VacinaDAO vacinaDao = new VacinaDAO();
        
        Vacina vacina = (Vacina)vacinasComboBox.getSelectedItem();
        Vacinacao vacinacao = new Vacinacao(data,fila.get(0).getRg(),vacina.getCodigo(),this.usuario.getLogin());
        vacinacaoDao.cadastraVacinacao(vacinacao);
        vacinaDao.DebitaVacina(vacina);
        
        JOptionPane.showMessageDialog(this, "Vacinação aplicada com Sucesso !");
        this.fila.remove(0);
        atualizaFila();
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Problema aqui");
            e.printStackTrace();
        }
    }//GEN-LAST:event_registraVacinacaoButtonActionPerformed

