    private void buscarVacinas(){
        try{
            VacinaDAO dao = new VacinaDAO();
            Vacina[] vacinas = dao.obtemVacinas();
            vacinasComboBox.setModel(new DefaultComboBoxModel<>(vacinas));
            
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Problema");
            
        }
    }
    
    private void buscarFila() {
        try{
            //filaTable.setModel(null);
            PessoaDAO pessoaDao = new PessoaDAO();
            
            List<Pessoa> pessoas = Arrays.asList(pessoaDao.obtemFila());
            //Collections.sort(pacientes);
            //Pessoa[] pessoas = pessoaDao.obtemPacientes();

            List<Pessoa> prioridade1 = new ArrayList<>();
            List<Pessoa> prioridade2 = new ArrayList<>();
            List<Pessoa> prioridade3 = new ArrayList<>();
            
           for(int i = 0; i < pessoas.size();i++){
               
               if(pessoas.get(i).getIdade()>= 70){
                   
                   prioridade1.add(pessoas.get(i));
                   
               }else if(pessoas.get(i).isTrabalhaComSaude()){
                   
                   prioridade2.add(pessoas.get(i));
                   
               }else {
                   prioridade3.add(pessoas.get(i));
               }
           }
           prioridade1.addAll(prioridade2);
           prioridade1.addAll(prioridade3);
           fila.addAll(prioridade1);
                        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Problema aqui");
        }
        
    }
    
    private void atualizaFila(){
        filaTable.setModel(new TableModel(fila));
        
        nomeLabel.setText(fila.get(0).getNome());
        idadeLabel.setText(String.valueOf(fila.get(0).getIdade()));
        rgLabel.setText(fila.get(0).getRg());     
        trabalhaSaudeCheckBox.setSelected(fila.get(0).isTrabalhaComSaude());
        trabalhaSaudeCheckBox.enableInputMethods(false);
        if(fila.get(0).getIdade()>= 70){ prioridadeLabel.setText("1");
        }else if(fila.get(0).isTrabalhaComSaude()){ prioridadeLabel.setText("2");
        }else { prioridadeLabel.setText("3");}
    }

